    @Test
    @Transactional
    void putNewClient() throws Exception {
        // Initialize the database
        clientRepository.saveAndFlush(client);

        int databaseSizeBeforeUpdate = clientRepository.findAll().size();

        // Update the client
        Client updatedClient = clientRepository.findById(client.getId()).get();
        // Disconnect from session so that the updates on updatedClient are not directly saved in db
        em.detach(updatedClient);
        updatedClient
            .numProd(UPDATED_NUM_PROD)
            .nomProd(UPDATED_NOM_PROD)
            .status(UPDATED_STATUS)
            .prixVente(UPDATED_PRIX_VENTE)
            .image(UPDATED_IMAGE)
            .imageContentType(UPDATED_IMAGE_CONTENT_TYPE)
            .ice(UPDATED_ICE);
        ClientDTO clientDTO = clientMapper.toDto(updatedClient);

        restClientMockMvc
            .perform(
                put(ENTITY_API_URL_ID, clientDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(clientDTO))
            )
            .andExpect(status().isOk());

        // Validate the Client in the database
        List<Client> clientList = clientRepository.findAll();
        assertThat(clientList).hasSize(databaseSizeBeforeUpdate);
        Client testClient = clientList.get(clientList.size() - 1);
        assertThat(testClient.getNumProd()).isEqualTo(UPDATED_NUM_PROD);
        assertThat(testClient.getNomProd()).isEqualTo(UPDATED_NOM_PROD);
        assertThat(testClient.getStatus()).isEqualTo(UPDATED_STATUS);
        assertThat(testClient.getPrixVente()).isEqualTo(UPDATED_PRIX_VENTE);
        assertThat(testClient.getImage()).isEqualTo(UPDATED_IMAGE);
        assertThat(testClient.getImageContentType()).isEqualTo(UPDATED_IMAGE_CONTENT_TYPE);
        assertThat(testClient.getIce()).isEqualTo(UPDATED_ICE);
    }

